    private void DatVeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DatVeActionPerformed
        if (clickedShowTime == null) {
            JOptionPane.showMessageDialog(
             null,
             "❌ Vui lòng chọn suất chiếu trước khi đặt vé!",
             "Lỗi",
             JOptionPane.ERROR_MESSAGE
            );
            return;
        }
        boolean hasSelectedSeat = false;
        for (JButton ghe : danhSachGhe) {
            if (ghe.getBackground().equals(Color.GREEN)) {
                hasSelectedSeat = true;
                break;
            }
        }
        if (!hasSelectedSeat) {
            JOptionPane.showMessageDialog(
             null,
             "Vui lòng chọn ít nhất một ghế trước khi đặt vé!",
             "Cảnh báo",
             JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(
         null,
         "Bạn có chắc muốn đặt các ghế đã chọn không?",
         "Xác nhận đặt vé",
         JOptionPane.YES_NO_OPTION,
         JOptionPane.QUESTION_MESSAGE
        );
        // Nếu người dùng hủy bỏ, thoát luôn
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }
        list.clear();
        // Duyệt qua danh sách ghế và đặt vé
        for (JButton ghe : danhSachGhe) {

            if (ghe.getBackground().equals(Color.GREEN)) {
                String seatName = ghe.getName();
                ghe.setBackground(Color.RED); // Đánh dấu ghế đã đặt

                String roomId = clickedShowTime.getScreenRoomId();
                String seatId = bookTicketUserController.getSeatId(seatName, roomId);
                String showTimeId = clickedShowTime.getId();

                String maVe = "VE" + UUID.randomUUID().toString().substring(0, 6).toUpperCase();
                try {
                    boolean result = bookTicketUserController.datVe(maVe, showTimeId, seatId, idPhimtoSuatChieu, account.getId());

                    if (!result) {
                        JOptionPane.showMessageDialog(
                         null,
                         "❌ Đặt vé thất bại cho ghế " + seatName + ".",
                         "Lỗi",
                         JOptionPane.ERROR_MESSAGE
                        );
                        ghe.setBackground(Color.GREEN); // Khôi phục màu nếu lỗi
                    } else {
                        list.add(maVe);
                    }
                } catch (Exception e) {
                    ghe.setBackground(Color.GREEN); // Khôi phục nếu lỗi
                    JOptionPane.showMessageDialog(
                     null,
                     "💥 Lỗi khi kết nối hoặc truy vấn CSDL với ghế " + seatName + ":\n" + e.getMessage(),
                     "Lỗi CSDL",
                     JOptionPane.ERROR_MESSAGE
                    );
                }
            }
        }
        int soluong = selectedButton.size();
        double tongTien = giaVeHienTai * soluong;
        String danhSachGhe = String.join(", ",
         selectedButton.stream()
          .map(JButton::getText)
          .toArray(String[]::new));
        String note = "Đã đặt " + soluong + " vé xem phim. Ghế: " + danhSachGhe;
        String maHD = "HD" + UUID.randomUUID().toString().substring(0, 6).toUpperCase();
        try {
            // 2. Đánh dấu ghế đã đặt (chỉ đổi màu)
            for (JButton ghe : selectedButton) {
                ghe.setBackground(Color.RED);
            }
            // 3. Tạo 1 hóa đơn duy nhất
            if (!bookTicketUserController.InsertHoaDon(maHD, account.getId(), tongTien, note)) {
                throw new Exception("Tạo hóa đơn thất bại");
            }
            JOptionPane.showMessageDialog(null, "Đặt vé thành công!", "Thành công", JOptionPane.INFORMATION_MESSAGE);
            for (String maVe : list) {
                bookTicketUserController.InsertChiTietHoaDon(maHD, maVe);
            }
        } catch (Exception e) {
            // Khôi phục màu ghế nếu lỗi
            for (JButton ghe : selectedButton) {
                ghe.setBackground(Color.GREEN);
            }
            JOptionPane.showMessageDialog(null, "Lỗi: " + e.getMessage(), "Thất bại", JOptionPane.ERROR_MESSAGE);
        } finally {
            // 4. Reset giao diện
            selectedButton.clear();
            CountSeat.setText("0");
            PriceSeatLabel.setText("0đ");
            Tongtien.setText("0đ");
        }
    }//GEN-LAST:event_DatVeActionPerformed

    private void ShowTimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ShowTimeActionPerformed
        if (!isComboBoxReady) {
            return;
        }
        String suatChieuDuocChon = (String) ShowTime.getSelectedItem();
        if (suatChieuDuocChon != null && suatChieuToPhongMap.containsKey(suatChieuDuocChon)) {
            TenSuatChieu.setText(suatChieuDuocChon);
            String[] parts = suatChieuDuocChon.split(" - ");
            if (parts.length == 2) {
                String phongChieu = parts[1]; // phần "Phòng X"
                TenPC.setText(phongChieu);
            }
        }

        ArrayList<Showtime> list;
        try {
            int index = ShowTime.getSelectedIndex();

            if (index < 0) {
                return; //Không có mục nào được chọn
            }

            //Lấy danh sách suất chiếu từ db
            list = (ArrayList<Showtime>) bookTicketUserController.showShowTime(idPhimtoSuatChieu);

            //Lấy suất chiếu tương ứng với index
            Showtime item = list.get(index);
            clickedShowTime = item;
            giaVeHienTai = clickedShowTime.getTicketPrice();
            System.out.println(item.getId());

            //Lấy danh sách tên các ghế đã được đặt
            ArrayList<String> danhSachGheDaDat = bookTicketUserController.searchSeatNameFromsShowTime("Dadat", item.getId());
            if (danhSachGheDaDat == null) {
                danhSachGheDaDat = new ArrayList<>();
            }
            System.out.println(danhSachGheDaDat.toString());
            for (JButton ghe : danhSachGhe) {
                if (danhSachGheDaDat.indexOf(ghe.getText()) != -1) {
                    ghe.setBackground(Color.red);
                } else {
                    ghe.setBackground(UIManager.getColor("Button.background"));
                }
            }
        } catch (Exception e1) {
        }

        selectedButton.clear();
        CountSeat.setText("");
        PriceSeatLabel.setText("");
        Tongtien.setText("");

    }//GEN-LAST:event_ShowTimeActionPerformed

