    private void hienThiSuatChieu(String tenPhim) {
        isComboBoxReady = false;
        List<ShowtimeInfor> danhSachSuatChieu = bookTicketUserController.getShowTime(tenPhim);

        ShowTime.removeAllItems();
        suatChieuToPhongMap.clear();

        for (ShowtimeInfor suat : danhSachSuatChieu) {
            String gioBatDau = suat.getStartTime() + " - " + suat.getName();
            ShowTime.addItem(gioBatDau);
            suatChieuToPhongMap.put(gioBatDau, suat.getName());
            TenPC.setText(danhSachSuatChieu.get(0).getName());
        }
        isComboBoxReady = true;
    }

    private void taoGhe() {
        SEAT.setLayout(new GridLayout(5, 8, 5, 5));
        SEAT.setBorder(BorderFactory.createEmptyBorder(10, 10, 10, 10));
        String[] hangGhe = {"A", "B", "C", "D", "E"};
        for (String hang : hangGhe) {
            for (int col = 1; col <= 8; col++) {
                String gheText = hang + col;
                JButton button = new JButton(gheText);
                button.setBackground(UIManager.getColor("Button.background"));
                button.setName(gheText);
                button.setBackground(UIManager.getColor("Button.background"));

                button.addActionListener(new ActionListener() {
                    @Override

                    public void actionPerformed(ActionEvent e) {
                        if (button.getBackground().equals(Color.RED)) {
                            return;
                        } else if (button.getBackground().equals(Color.GREEN)) {
                            button.setBackground(UIManager.getColor("Button.background"));
                            selectedButton.remove(button);
                        } else {
                            button.setBackground(Color.green);
                            selectedButton.add(button);
                        }

                        int soLuong = selectedButton.size();
                        double tongTien = giaVeHienTai * soLuong;

                        CountSeat.setText(String.valueOf(soLuong));
                        PriceSeatLabel.setText(String.format("%,.0f", tongTien) + "đ");
                        Tongtien.setText(String.format("%,.0f", tongTien) + "đ");
                    }
                });

                button.setFont(new Font("Arial", Font.BOLD, 12));
                SEAT.add(button);
                danhSachGhe.add(button);
            }
        }
    }


