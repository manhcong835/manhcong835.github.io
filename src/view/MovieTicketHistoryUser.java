    public void search(DefaultTableModel table, String maTaiKhoan, String tenPhim) {
        List<Object[]> ketQua = movieTicketHistoryUserController.timKiemHoaDonTheoPhim(maTaiKhoan, tenPhim);
        if (ketQua != null && !ketQua.isEmpty()) {
            for (Object[] row : ketQua) {
                Object[] formattedRow = Arrays.copyOf(row, row.length);
                if (formattedRow.length > 6 && formattedRow[6] instanceof Number) {
                    formattedRow[6] = String.format("%,d VND", ((Number) formattedRow[6]).intValue());
                }
                table.addRow(formattedRow);
            }
        } else {
            JOptionPane.showMessageDialog(null,
                    "Không tìm thấy lịch sử vé cho phim: " + tenPhim + "\nKiểm tra lại tên phim hoặc mã tài khoản.",
                    "Thông báo",
                    JOptionPane.INFORMATION_MESSAGE);
        }
    }


