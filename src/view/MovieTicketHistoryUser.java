    public void loadMovieTicketHistory(DefaultTableModel table, String idTaiKhoan) {
        List<Object[]> invoiceData = movieTicketHistoryUserController.getInvoiceInfor(idTaiKhoan);
        table.setRowCount(0);
        if (invoiceData != null && !invoiceData.isEmpty()) {
            for (Object[] row : invoiceData) {
                // Định dạng lại dữ liệu nếu cần
                Object[] formattedRow = Arrays.copyOf(row, row.length);

                // Định dạng tiền tệ (giả sử cột tổng tiền là vị trí số 6)
                if (formattedRow.length > 6 && formattedRow[6] instanceof Number) {
                    formattedRow[6] = String.format("%,d VND", ((Number) formattedRow[6]).intValue());
                }

                table.addRow(formattedRow);
            }
        } else {
            // Hiển thị thông báo nếu không có dữ liệu
//        JOptionPane.showMessageDialog(null, 
//            "Không tìm thấy lịch sử vé cho suất chiếu này", 
//            "Thông báo", 
//            JOptionPane.INFORMATION_MESSAGE);
        }
    }

